//-----------------------------------------------------------------------------------------------
QuCVector3::QuCVector3(const QuCVector2& _rg, byte _b)
	: r(_rg.r)
	, g(_rg.g)
	, b(_b)
{}


//-----------------------------------------------------------------------------------------------
QuCVector3::QuCVector3(byte _r, const QuCVector2& _gb)
	: r(_r)
	, g(_gb.r)
	, b(_gb.g)
{}


//-----------------------------------------------------------------------------------------------
//END CTORS AND DTOR
//-----------------------------------------------------------------------------------------------


//-----------------------------------------------------------------------------------------------
QuCVector2 QuCVector3::XY() const
{
	return QuCVector2(r, g);
}


